public class student {
    private String name;
    private String math;
    private String chinese;
    private String english;
    private String science;
    private String gym;

    public String getName() { return name; }
    public void setName(String name) {this.name = name; }

    public String getMath() {return math;}
    public void setMath(String math) {this.math = math;}

    public String getChinese() {return chinese; }
    public void setChinese(String chinese) {this.chinese = chinese;}

    public String getEnglish() { return english;}
    public void setEnglish(String english) {this.english = english;}

    public String getScience() {return science;}
    public void setScience(String science) {this.science = science;}

    public String getGym() {return gym;}
    public void setGym(String gym) {this.gym = gym;}
}
